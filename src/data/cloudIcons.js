// Helper: get all categories
export const getCategories = () => {
  const cats = new Set();
  cloudIcons.forEach(icon => cats.add(icon.category));
  return Array.from(cats);
};

// Helper: get all providers
export const getProviders = () => {
  const provs = new Set();
  cloudIcons.forEach(icon => provs.add(icon.provider));
  return Array.from(provs);
};

// Helper: filter icons by provider and/or category
export const filterIcons = (provider, category) => {
  return cloudIcons.filter(icon => {
    const matchProvider = !provider || provider === 'all' || icon.provider === provider;
    const matchCategory = !category || category === 'all' || icon.category === category;
    return matchProvider && matchCategory;
  });
};

// Helper: get icon by id
export const getIconById = (id) => {
  return cloudIcons.find(icon => icon.id === id);
};

